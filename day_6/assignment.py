# Banking Application
# Amount -> users amount in bank
# Deposit -> Add money in bank account
#         -> Amount should be added in balance.
#         -> Amount should ALWAYS be positive. 
# Withdraw -> Withdraw money from users account
#          -> Amount should be POSITIVE
#          -> Amount < Bank Balance
#          -> Deduct from Bank Balance
# Check Balance -> Gives user's bank balance
#               -> Give out users remaining amount of money
# Main Menu: Deposit, WIthdraw and Check Balance
# Infinite App unless user ends it.